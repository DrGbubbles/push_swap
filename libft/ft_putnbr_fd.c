#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	x;

	x = n;
	if (x < 0)
	{
		x *= -1;
		ft_putchar_fd('-', fd);
	}
	if (x >= 10)
		ft_putnbr_fd((x / 10), fd);
	ft_putchar_fd(((x % 10) + '0'), fd);
}
